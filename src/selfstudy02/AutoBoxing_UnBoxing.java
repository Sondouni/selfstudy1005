package day0917;

public class AutoBoxing_UnBoxing {

	public static void main(String[] args) {
		//AutoBoxing 
		Integer i =10;
		Double d = 3.14;
		Float f = 3.14f;
		Character c = 'A';
		
		//Auto unBoxing
		int ii = i;
		double dd = d;
		float ff = f;
		char cc = c;
		
		System.out.println(i);
		System.out.println(ii);
	}

}
